package mx.com.iusacell.services.miiusacell.vo;

public class DatosLineaInsuranceVO {
	private String fechaContratacion;
	private String fechaInicio;
	private String statusLinea;
	private String descripcionPlan;
	
	public String getFechaContratacion() {
		return fechaContratacion;
	}
	public void setFechaContratacion(final String fechaContratacion) {
		this.fechaContratacion = fechaContratacion;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(final String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getStatusLinea() {
		return statusLinea;
	}
	public void setStatusLinea(final String statusLinea) {
		this.statusLinea = statusLinea;
	}
	public String getDescripcionPlan() {
		return descripcionPlan;
	}
	public void setDescripcionPlan(final String descripcionPlan) {
		this.descripcionPlan = descripcionPlan;
	}
}
