package mx.com.iusacell.services.miiusacell.vo;

public class IndicadoresVO {
	
	private String cantidad;
	private String dinero;
	private String categoria;
	private String tipoLinea;
	private String compania;
	private String fecha;
	private String hora;
	private String plataforma;
	
	public String getCantidad() {
		return cantidad;
	}
	public void setCantidad(final String cantidad) {
		this.cantidad = cantidad;
	}
	public String getDinero() {
		return dinero;
	}
	public void setDinero(final String dinero) {
		this.dinero = dinero;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(final String categoria) {
		this.categoria = categoria;
	}
	
	public String getTipoLinea() {
		return tipoLinea;
	}
	public void setTipoLinea(final String tipoLinea) {
		this.tipoLinea = tipoLinea;
	}
	public String getCompania() {
		return compania;
	}
	public void setCompania(final String compania) {
		this.compania = compania;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(final String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(final String hora) {
		this.hora = hora;
	}
	public String getPlataforma() {
		return plataforma;
	}
	public void setPlataforma(final String plataforma) {
		this.plataforma = plataforma;
	}	
}
