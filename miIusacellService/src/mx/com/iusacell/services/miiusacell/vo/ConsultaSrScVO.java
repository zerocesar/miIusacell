package mx.com.iusacell.services.miiusacell.vo;

public class ConsultaSrScVO {
	private String code;
	private String descripcion;
	private String fechaFinRecurrenciaActual;
	private String fechaFinal;
	private String fechaInicial;
	private String messageCode;
	private String recurrenciaActual;
	private String recurrenciaTotales;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getFechaFinRecurrenciaActual() {
		return fechaFinRecurrenciaActual;
	}
	public void setFechaFinRecurrenciaActual(String fechaFinRecurrenciaActual) {
		this.fechaFinRecurrenciaActual = fechaFinRecurrenciaActual;
	}
	public String getFechaFinal() {
		return fechaFinal;
	}
	public void setFechaFinal(String fechaFinal) {
		this.fechaFinal = fechaFinal;
	}
	public String getFechaInicial() {
		return fechaInicial;
	}
	public void setFechaInicial(String fechaInicial) {
		this.fechaInicial = fechaInicial;
	}
	public String getMessageCode() {
		return messageCode;
	}
	public void setMessageCode(String messageCode) {
		this.messageCode = messageCode;
	}
	public String getRecurrenciaActual() {
		return recurrenciaActual;
	}
	public void setRecurrenciaActual(String recurrenciaActual) {
		this.recurrenciaActual = recurrenciaActual;
	}
	public String getRecurrenciaTotales() {
		return recurrenciaTotales;
	}
	public void setRecurrenciaTotales(String recurrenciaTotales) {
		this.recurrenciaTotales = recurrenciaTotales;
	}
}
