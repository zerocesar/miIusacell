package mx.com.iusacell.services.miiusacell.vo.autorizador;

import java.io.Serializable;
import java.util.Calendar;

public class CardVO implements Serializable{
	
	private static final long serialVersionUID = 1L;
	private String cardNumber;							
	private Calendar creationDate;			
	private String cvv2;
	private String expirationMonth;
	private String expirationYear;	
	private int type;
	
	public String getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(final String cardNumber) {
		this.cardNumber = cardNumber;
	}
	public Calendar getCreationDate() {
		return creationDate;
	}
	public void setCreationDate(final Calendar creationDate) {
		this.creationDate = creationDate;
	}
	public String getCvv2() {
		return cvv2;
	}
	public void setCvv2(final String cvv2) {
		this.cvv2 = cvv2;
	}
	public String getExpirationMonth() {
		return expirationMonth;
	}
	public void setExpirationMonth(final String expirationMonth) {
		this.expirationMonth = expirationMonth;
	}
	public String getExpirationYear() {
		return expirationYear;
	}
	public void setExpirationYear(final String expirationYear) {
		this.expirationYear = expirationYear;
	}
	public int getType() {
		return type;
	}
	public void setType(final int type) {
		this.type = type;
	}		
}
