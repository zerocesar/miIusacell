package mx.com.iusacell.services.miiusacell.vo;

public class OperadorVO {

	private String idOperadorComp;
	private String idA;
	private String idO;
	private String portado;
	private String idEstatusNum;
	private String operaInt;
	
	public String getIdOperadorComp() {
		return idOperadorComp;
	}
	public void setIdOperadorComp(final String idOperadorComp) {
		this.idOperadorComp = idOperadorComp;
	}
	public String getIdA() {
		return idA;
	}
	public void setIdA(final String idA) {
		this.idA = idA;
	}
	public String getIdO() {
		return idO;
	}
	public void setIdO(final String idO) {
		this.idO = idO;
	}
	public String getPortado() {
		return portado;
	}
	public void setPortado(final String portado) {
		this.portado = portado;
	}
	public String getIdEstatusNum() {
		return idEstatusNum;
	}
	public void setIdEstatusNum(final String idEstatusNum) {
		this.idEstatusNum = idEstatusNum;
	}
	public String getOperaInt() {
		return operaInt;
	}
	public void setOperaInt(String operaInt) {
		this.operaInt = operaInt;
	}
}
