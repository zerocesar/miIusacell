package mx.com.iusacell.services.miiusacell.vo;

public class PinEstatus {
	
	private String pin;
	private int estatus;
	
	private String mensaje;
	private String operador;
	private int estatusBD;
	private String mensajeBD;
	public String getPin() {
		return pin;
	}
	public void setPin(final String pin) {
		this.pin = pin;
	}
	public int getEstatus() {
		return estatus;
	}
	public void setEstatus(final int estatus) {
		this.estatus = estatus;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(final String mensaje) {
		this.mensaje = mensaje;
	}
	public String getOperador() {
		return operador;
	}
	public void setOperador(final String operador) {
		this.operador = operador;
	}
	public int getEstatusBD() {
		return estatusBD;
	}
	public void setEstatusBD(final int estatusBD) {
		this.estatusBD = estatusBD;
	}
	public String getMensajeBD() {
		return mensajeBD;
	}
	public void setMensajeBD(final String mensajeBD) {
		this.mensajeBD = mensajeBD;
	}
	
	

}
