package mx.com.iusacell.services.miiusacell.vo;

public class RespuestaVO {

	private String  cdgRespuesta;
	private String  mensaje;
	private boolean exito;
	private String  folio;
	
	public String getCdgRespuesta() {
		return cdgRespuesta;
	}
	public void setCdgRespuesta(final String cdgRespuesta) {
		this.cdgRespuesta = cdgRespuesta;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(final String mensaje) {
		this.mensaje = mensaje;
	}
	public boolean isExito() {
		return exito;
	}
	public void setExito(final boolean exito) {
		this.exito = exito;
	}
	public String getFolio() {
		return folio;
	}
	public void setFolio(final String folio) {
		this.folio = folio;
	}
			
}
