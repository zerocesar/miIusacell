package mx.com.iusacell.services.miiusacell.vo;

public class PuntosVO {
	
	private String nombreFamilia;
	private String nombrePlan;
	private double planSIva;
	private double puntos;
	private double premioPor;
	private double puntosATM;
	public String getNombreFamilia() {
		return nombreFamilia;
	}
	public void setNombreFamilia(final String nombreFamilia) {
		this.nombreFamilia = nombreFamilia;
	}
	public String getNombrePlan() {
		return nombrePlan;
	}
	public void setNombrePlan(final String nombrePlan) {
		this.nombrePlan = nombrePlan;
	}
	public double getPlanSIva() {
		return planSIva;
	}
	public void setPlanSIva(final double planSIva) {
		this.planSIva = planSIva;
	}
	public double getPuntos() {
		return puntos;
	}
	public void setPuntos(final double puntos) {
		this.puntos = puntos;
	}
	public double getPremioPor() {
		return premioPor;
	}
	public void setPremioPor(final double premioPor) {
		this.premioPor = premioPor;
	}
	public double getPuntosATM() {
		return puntosATM;
	}
	public void setPuntosATM(final double puntosATM) {
		this.puntosATM = puntosATM;
	}
	

}
