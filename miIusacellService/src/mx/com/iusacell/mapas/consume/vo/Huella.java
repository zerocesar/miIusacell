package mx.com.iusacell.mapas.consume.vo;

public class Huella {

  private Integer formatoHuella;
  private String  hashHuella;
  private Integer posHuella;

  public Huella() {
    formatoHuella = 0;
    posHuella = 0;
  }

  public void setFormatoHuella(Integer formatoHuella) {
    this.formatoHuella = formatoHuella;
  }

  public Integer getFormatoHuella() {
    return formatoHuella;
  }

  public void setHashHuella(String hashHuella) {
    this.hashHuella = hashHuella;
  }

  public String getHashHuella() {
    return hashHuella;
  }

  public void setPosHuella(Integer posHuella) {
    this.posHuella = posHuella;
  }

  public Integer getPosHuella() {
    return posHuella;
  }

}
